from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
RULES_FILE = ROOT / "rules" / "custom-direct.txt"
DIST_FILE = ROOT / "dist" / "whitelist-overlay.yaml"

PROVIDERS = [
    ("applications", "classical", "applications.txt"),
    ("private", "domain", "private.txt"),
    ("direct", "domain", "direct.txt"),
    ("lancidr", "ipcidr", "lancidr.txt"),
    ("cncidr", "ipcidr", "cncidr.txt"),
]


def load_custom_rules() -> list[str]:
    rules: list[str] = []
    for raw_line in RULES_FILE.read_text(encoding="utf-8").splitlines():
        line = raw_line.strip()
        if not line or line.startswith("#"):
            continue
        rules.append(line)
    return rules


def render_rule_providers(base_url: str) -> str:
    lines = ["rule-providers:\n"]
    for name, behavior, filename in PROVIDERS:
        lines.extend(
            [
                f"  {name}:\n",
                "    type: http\n",
                f"    behavior: {behavior}\n",
                "    format: text\n",
                f"    path: ./ruleset/loyalsoldier/{filename}\n",
                "    interval: 86400\n",
                f"    url: {base_url.rstrip('/')}/{filename}\n",
            ]
        )
    return "".join(lines)


