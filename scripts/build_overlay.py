def render_rules(custom_rules: list[str]) -> str:
    lines = ["rules:\n"]
    for rule in custom_rules:
        lines.append(f"  - {rule}\n")
    lines.extend(
        [
            "  - RULE-SET,applications,DIRECT\n",
            "  - RULE-SET,private,DIRECT\n",
            "  - RULE-SET,direct,DIRECT\n",
            "  - RULE-SET,lancidr,DIRECT,no-resolve\n",
            "  - RULE-SET,cncidr,DIRECT,no-resolve\n",
            "  - GEOIP,LAN,DIRECT,no-resolve\n",
            "  - GEOIP,CN,DIRECT,no-resolve\n",
            "  - MATCH,代理模式\n",
        ]
    )
    return "".join(lines)


def build(base_url: str = "https://083105.xyz/rules/loyalsoldier") -> None:
    DIST_FILE.parent.mkdir(parents=True, exist_ok=True)
    custom_rules = load_custom_rules()
    output = [
        "# Generated by scripts/build_overlay.py\n",
        render_dns(),
        "\n",
        render_rule_providers(base_url),
        "\n",
        render_proxy_groups(),
        "\n",
        render_rules(custom_rules),
    ]
    DIST_FILE.write_text("".join(output), encoding="utf-8")
    print(f"wrote {DIST_FILE}")


if __name__ == "__main__":
    build()

